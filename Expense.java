    private void getEntries() {
    try {
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) Table.getModel();
        dtm.setRowCount(0); // Clear existing rows
        java.util.Date now = new java.util.Date();
        java.util.Date lastMonth = new java.util.Date(now.getTime() - (30L * 24 * 60 * 60 * 1000)); // 30 days ago

        List<expense_model> expenses = expenseService.getExpenses(lastMonth, now);
        int total = 0;
        for (expense_model expense : expenses) {
            total += expense.getAmount();
            dtm.addRow(new Object[]{
                expense.getId(),
                expense.getSpendingDate(),
                expense.getCategory(),
                expense.getAmount()
            });
        }
        totalamount.setText(String.valueOf(total));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}


